'use strict';

/**
 * agents/lib/codegen-templates.js
 * --------------------------------------------------------------------------
 * Pure string templates for Layer 4 code generation. No I/O here — the agent
 * decides what to write; this module only renders guard-rule-compliant
 * Playwright TypeScript from inputs.
 *
 * Guard rules honored by the rendered code:
 *  - Selectors live ONLY in the Page Object, never in the spec.
 *  - Web-first, auto-retrying assertions (expect(locator).toBeVisible()).
 *  - Explicit waits via locator.waitFor; no page.waitForTimeout().
 *  - Tests isolated with per-test pages and before/after hooks.
 */

// --- naming helpers --------------------------------------------------------

const NOISE_SUFFIXES = ['-select', '-input', '-btn', '-button', '-field', '-dropdown'];

function camel(str) {
  return str
    .replace(/[^a-zA-Z0-9]+(.)?/g, (_, c) => (c ? c.toUpperCase() : ''))
    .replace(/^[A-Z]/, (c) => c.toLowerCase());
}

function pascal(str) {
  const c = camel(str);
  return c.charAt(0).toUpperCase() + c.slice(1);
}

/** Leading tag name of a CSS selector, e.g. 'select[data-testid="x"]' -> 'select'. */
function tagOf(selector) {
  const m = selector.match(/^([a-zA-Z]+)/);
  return m ? m[1].toLowerCase() : 'element';
}

/** data-testid value if present, else null. */
function testIdOf(selector) {
  const m = selector.match(/data-testid=['"]([^'"]+)['"]/);
  return m ? m[1] : null;
}

/**
 * Derive a locator property name and an action base name from a selector.
 * teacher-select -> { prop: 'teacherSelect', base: 'teacher' }
 */
function namesFor(selector, index) {
  const testId = testIdOf(selector);
  if (!testId) {
    const prop = `element${index + 1}`;
    return { prop, base: prop };
  }
  const prop = camel(testId);
  let baseRaw = testId;
  for (const suffix of NOISE_SUFFIXES) {
    if (baseRaw.endsWith(suffix)) {
      baseRaw = baseRaw.slice(0, -suffix.length);
      break;
    }
  }
  return { prop, base: camel(baseRaw) };
}

/** Build the locator + action method descriptors for a set of selectors. */
function describeLocators(selectors) {
  return selectors.map((selector, i) => {
    const { prop, base } = namesFor(selector, i);
    return { selector, prop, base, Base: pascal(base), Prop: pascal(prop), tag: tagOf(selector) };
  });
}

// --- Page Object renderer --------------------------------------------------

function renderActionMethods(locators) {
  const blocks = [];
  for (const loc of locators) {
    if (loc.tag === 'select') {
      blocks.push(
        `  async select${loc.Base}(value: string): Promise<void> {\n` +
          `    await this.${loc.prop}.waitFor({ state: 'visible', timeout: this.timeout });\n` +
          `    await this.${loc.prop}.selectOption({ label: value });\n  }`
      );
    } else if (loc.tag === 'input' || loc.tag === 'textarea') {
      blocks.push(
        `  async fill${loc.Base}(value: string): Promise<void> {\n` +
          `    await this.${loc.prop}.waitFor({ state: 'visible', timeout: this.timeout });\n` +
          `    await this.${loc.prop}.fill(value);\n  }`
      );
    } else if (loc.tag === 'button' || loc.tag === 'a') {
      blocks.push(
        `  async click${loc.Base}(): Promise<void> {\n` +
          `    await this.${loc.prop}.click();\n  }`
      );
    } else {
      blocks.push(
        `  async get${loc.Prop}Text(): Promise<string | null> {\n` +
          `    await this.${loc.prop}.waitFor({ state: 'visible', timeout: this.timeout });\n` +
          `    return this.${loc.prop}.textContent();\n  }\n\n` +
          `  async is${loc.Prop}Visible(): Promise<boolean> {\n` +
          `    return this.${loc.prop}.isVisible();\n  }`
      );
    }
  }
  return blocks.join('\n\n');
}

function renderPageObject({ className, sourceFile, locators }) {
  const fields = locators.map((l) => `  readonly ${l.prop}: Locator;`).join('\n');
  const wiring = locators.map((l) => `    this.${l.prop} = page.locator('${l.selector}');`).join('\n');

  return `import { Page, Locator } from '@playwright/test';

/**
 * ${className}
 * Auto-generated by Layer 4 (Playwright Automation Engineer).
 *
 * Selectors below are REUSED VERBATIM from ${sourceFile}, surfaced by the
 * Layer 3 vector store, so DOM bindings stay consistent across the suite.
 * Review and rename generated action methods to match the feature vocabulary.
 */
export class ${className} {
  readonly page: Page;
  private readonly timeout = 10000;
${fields}

  constructor(page: Page) {
    this.page = page;
${wiring}
  }

  async navigateTo(path: string = '/'): Promise<void> {
    await this.page.goto(path);
    await this.page.waitForLoadState('networkidle');
  }

${renderActionMethods(locators)}
}
`;
}

// --- Spec renderer ---------------------------------------------------------

function renderTestBlock(testCase, locators) {
  const id = testCase.id || 'TC';
  const category = (testCase.category || 'POSITIVE').toUpperCase();
  const desc = testCase.description || 'Verify behavior';
  const priority = testCase.priority || 'MEDIUM';

  // Pick a sensible assertion target from the available locators.
  const errorLoc = locators.find((l) => /error/i.test(l.prop));
  const successLoc = locators.find((l) => /confirm|success|result/i.test(l.prop));
  const isNegative = category === 'NEGATIVE';
  const target = isNegative ? errorLoc : successLoc || locators.find((l) => l.tag !== 'button');

  const assertion = target
    ? `    // ${isNegative ? 'Expect the error state to surface' : 'Expect the success state to render'}\n` +
      `    expect(await pageObject.is${target.Prop}Visible()).toBe(${isNegative ? 'true' : 'true'});`
    : `    // TODO: add a web-first assertion via a pageObject getter`;

  return `  // ${id} [${category}] priority: ${priority}
  test('${id}: ${desc.replace(/'/g, "\\'")}', async () => {
    // ARRANGE / ACT — drive the flow through reused Page Object methods.
    // TODO: fill in the steps from the acceptance criteria, e.g.:
    //   await pageObject.select<Field>('value');
    //   await pageObject.click<Action>();

    // ASSERT
${assertion}
  });`;
}

function renderSpec({ describeTitle, className, pomImportPath, dbImportPath, locators, testCases }) {
  const blocks = (testCases.length ? testCases : [{ id: 'TC-01', category: 'POSITIVE', description: 'Execute main workflow successfully', priority: 'CRITICAL' }])
    .map((tc) => renderTestBlock(tc, locators))
    .join('\n\n');

  return `import { test, expect, Page } from '@playwright/test';
import { ${className} } from '${pomImportPath}';

/**
 * ${describeTitle}
 * Auto-generated by Layer 4. Imports the reused ${className} Page Object.
 * Selectors stay encapsulated in the POM.
 */

test.describe('${describeTitle}', () => {
  let page: Page;
  let pageObject: ${className};

  test.beforeEach(async ({ browser }) => {
    page = await browser.newPage();
    pageObject = new ${className}(page);
    await pageObject.navigateTo();
  });

  test.afterEach(async () => {
    await page.close();
  });

${blocks}
});
`;
}

module.exports = { describeLocators, renderPageObject, renderSpec, pascal, camel };
