#!/usr/bin/env node

/**
 * LAYER 0: Ticket Intake & PR Creation
 * --------------------------------------------------------------------------
 * Fetches Jira ticket, creates feature branch, opens draft pull request
 * on GitHub, and feeds ticket data to downstream layers.
 *
 * Usage:
 *   node agents/agent-layer0-ticket-intake.js <ISSUE-KEY>
 *   node agents/agent-layer0-ticket-intake.js ISE-1234
 *
 * Environment variables:
 *   JIRA_DOMAIN, JIRA_USER_EMAIL, JIRA_API_TOKEN (for Jira)
 *   GITHUB_TOKEN (GitHub personal access token)
 *   GITHUB_OWNER (GitHub org/user)
 *   GITHUB_REPO (Repository name)
 */

'use strict';

require('dotenv').config();
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

// Import Jira functions (reuse existing jira-fetch module)
const axios = require('axios');

// --- Configuration ---------------------------------------------------------

const JIRA_DOMAIN = process.env.JIRA_DOMAIN;
const JIRA_USER_EMAIL = process.env.JIRA_USER_EMAIL;
const JIRA_API_TOKEN = process.env.JIRA_API_TOKEN;

// GitHub: GITHUB_OWNER must be username (NOT email). Find at https://github.com/settings/profile
const GITHUB_TOKEN = process.env.GITHUB_TOKEN;
const GITHUB_OWNER = process.env.GITHUB_OWNER;
const GITHUB_REPO = process.env.GITHUB_REPO;

// --- Jira Client -----------------------------------------------------------

function buildJiraClient() {
  const missing = [];
  if (!JIRA_DOMAIN) missing.push('JIRA_DOMAIN');
  if (!JIRA_USER_EMAIL) missing.push('JIRA_USER_EMAIL');
  if (!JIRA_API_TOKEN) missing.push('JIRA_API_TOKEN');

  if (missing.length) {
    throw new Error(`Missing Jira env vars: ${missing.join(', ')}`);
  }

  const host = JIRA_DOMAIN.replace(/^https?:\/\//, '').replace(/\/+$/, '');
  return axios.create({
    baseURL: `https://${host}/rest/api/3`,
    auth: { username: JIRA_USER_EMAIL, password: JIRA_API_TOKEN },
    headers: { Accept: 'application/json' },
    timeout: 30000,
    validateStatus: (status) => status >= 200 && status < 300,
  });
}

// --- Jira Operations -------------------------------------------------------

async function fetchIssue(client, issueKey) {
  try {
    const { data } = await client.get(`/issue/${encodeURIComponent(issueKey)}`, {
      params: { fields: 'summary,description,status,assignee,priority,labels' },
    });
    return data;
  } catch (err) {
    throw new Error(`Failed to fetch issue ${issueKey}: ${err.message}`);
  }
}

// --- GitHub Operations -----------------------------------------------------

async function createGitHubPR(ticketKey, ticketSummary) {
  if (!GITHUB_TOKEN || !GITHUB_OWNER || !GITHUB_REPO) {
    console.warn('⚠️  Skipping PR creation: missing GITHUB_TOKEN, GITHUB_OWNER, or GITHUB_REPO');
    return null;
  }

  try {
    const client = axios.create({
      baseURL: 'https://api.github.com',
      headers: {
        Authorization: `Bearer ${GITHUB_TOKEN}`,
        Accept: 'application/vnd.github.v3+json',
      },
      timeout: 30000,
    });

    const branchName = `feature/${ticketKey.toLowerCase()}`;
    const prTitle = `[${ticketKey}] ${ticketSummary}`;
    const prBody = `
## Ticket
Jira: ${ticketKey}

## Summary
${ticketSummary}

## Status
- [ ] Tests generated (Layer 4)
- [ ] Tests executed (Layer 5)
- [ ] Tests passing
- [ ] Self-healing applied (Layer 6)

---
Generated by Layer 0 Ticket Intake
    `.trim();

    const response = await client.post(`/repos/${GITHUB_OWNER}/${GITHUB_REPO}/pulls`, {
      title: prTitle,
      body: prBody,
      head: branchName,
      base: 'main',
      draft: true,
    });

    return {
      number: response.data.number,
      url: response.data.html_url,
      id: response.data.id,
    };
  } catch (err) {
    if (err.response?.status === 422) {
      console.warn(`⚠️  PR already exists or branch not found. Continuing...`);
      return null;
    }
    throw new Error(`Failed to create GitHub PR: ${err.message}`);
  }
}

// --- Git Operations --------------------------------------------------------

function createFeatureBranch(ticketKey) {
  const branchName = `feature/${ticketKey.toLowerCase()}`;

  try {
    // Check if branch exists
    try {
      execSync(`git rev-parse --verify ${branchName}`, { stdio: 'ignore' });
      console.log(`✅ Branch '${branchName}' already exists`);
      return branchName;
    } catch {
      // Branch doesn't exist, create it
    }

    // Create branch from main
    execSync('git fetch origin main', { stdio: 'inherit' });
    execSync(`git checkout -b ${branchName} origin/main`, { stdio: 'inherit' });
    console.log(`✅ Created feature branch: ${branchName}`);
    return branchName;
  } catch (err) {
    throw new Error(`Failed to create feature branch: ${err.message}`);
  }
}

// --- Output & Storage ------------------------------------------------------

function saveTicketContext(ticketKey, ticketData, prInfo) {
  const contextDir = path.join(__dirname, '..', 'context');
  if (!fs.existsSync(contextDir)) {
    fs.mkdirSync(contextDir, { recursive: true });
  }

  const contextFile = path.join(contextDir, 'layer0-ticket-intake.json');
  const fields = ticketData.fields || {};

  const context = {
    timestamp: new Date().toISOString(),
    ticketKey: ticketKey,
    summary: fields.summary || 'No summary',
    description: fields.description || 'No description',
    status: fields.status ? fields.status.name : 'Unknown',
    assignee: fields.assignee ? fields.assignee.displayName : 'Unassigned',
    priority: fields.priority ? fields.priority.name : 'None',
    labels: fields.labels || [],
    pr: prInfo || null,
  };

  fs.writeFileSync(contextFile, JSON.stringify(context, null, 2));
  console.log(`✅ Ticket context saved to: ${contextFile}`);

  return context;
}

// --- Main Pipeline ---------------------------------------------------------

async function main() {
  const ticketKey = process.argv[2];

  if (!ticketKey) {
    throw new Error('Usage: node agent-layer0-ticket-intake.js <ISSUE-KEY>');
  }

  console.log('\n╔════════════════════════════════════════════════════════════╗');
  console.log('║   LAYER 0: Ticket Intake & Pull Request Creation           ║');
  console.log('║   Jira → Git Branch → Draft PR                             ║');
  console.log('╚════════════════════════════════════════════════════════════╝\n');

  try {
    // 1. Fetch Jira ticket
    console.log(`\n📋 Fetching ticket: ${ticketKey}`);
    const jiraClient = buildJiraClient();
    const ticket = await fetchIssue(jiraClient, ticketKey);
    console.log(`✅ Ticket fetched: ${ticket.fields.summary}`);

    // 2. Create local feature branch
    console.log(`\n🌿 Creating feature branch...`);
    const branchName = createFeatureBranch(ticketKey);

    // 3. Create GitHub PR
    console.log(`\n📝 Creating draft pull request...`);
    const prInfo = await createGitHubPR(ticketKey, ticket.fields.summary);
    if (prInfo) {
      console.log(`✅ PR created: ${prInfo.url}`);
    }

    // 4. Save context for downstream layers
    console.log(`\n💾 Saving ticket context...`);
    const context = saveTicketContext(ticketKey, ticket, prInfo);

    // 5. Summary
    console.log('\n╔════════════════════════════════════════════════════════════╗');
    console.log('║                   LAYER 0 COMPLETE                         ║');
    console.log('╠════════════════════════════════════════════════════════════╣');
    console.log(`║ Ticket:         ${ticketKey}`);
    console.log(`║ Branch:         ${branchName}`);
    if (prInfo) {
      console.log(`║ PR:             #${prInfo.number} (Draft)`);
    }
    console.log('║ Status:         ✅ Ready for Layer 1');
    console.log('╚════════════════════════════════════════════════════════════╝\n');

    // Pass context to downstream layers
    process.exitCode = 0;
  } catch (err) {
    console.error(`\n❌ Layer 0 Failed: ${err.message}`);
    process.exitCode = 1;
  }
}

main();
